#while loop repeats block of statements while condition is TRUE
#   when condition is FALSE, while loop is ended
#   condition given before while loop is checked @ each execution of loop body
#while loop is used when it is impossible/inconvenient to determine exact num of loop iterations needed

#while loop syntax...
#while some condition:
#    a block of statements

#EXAMPLE:
i = 1
while i <= 5:
    print(i)
    i += 1
